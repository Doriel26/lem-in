#include "libft.h"

int		ft_nlen(intmax_t nbr)
{
	int i;

	i = 0;
	if (nbr > 0)
	{
		while (nbr >= 10)
		{
			nbr = nbr / 10;
			i++;
		}
	}
	else if (nbr < 0)
	{
		while (nbr <= -10)
		{
			nbr = nbr / 10;
			i++;
		}
		i++;
	}
	i++;
	return (i);
}
